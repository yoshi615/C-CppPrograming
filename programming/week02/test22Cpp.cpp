// 二つの変数xとyの合計と平均を表示
#include <iostream>
 using namespace std;
 int main()
 {
 int x; 
int y; 
x = 61;  
y = 19;  
// xはint型の変数
// yはint型の変数
// xに61を代入
// yに19を代入
cout << "xの値は" << x << "です。\n";   
cout << "yの値は" << y << "です。\n";   
cout << "合計は" << x + y << "です。\n";  
// xの値を表示
// yの値を表示
// xとyの合計を表示
cout << "平均は" << (x + y) / 2 << "です。\n"; 
}